package com.vinacredit.activity.Account;

public class ListItem {
	public int icon;
	public String title;
	public String subtitle;
	public ListItem (){
		super();
	}
	
	public ListItem (int icon, String title, String subtitle){
		super();
		this.icon 		= icon;
		this.title 		= title;
		this.subtitle 	= subtitle;
	}
}
